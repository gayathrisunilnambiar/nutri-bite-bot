def create_ifct_database():
    """Create IFCT database CSV from extracted data"""
    
    df = pd.DataFrame(IFCT_DATA)
    
    # Rename columns to match expected format
    df = df.rename(columns={
        'protein_g': 'protein_g_per_100g',
        'fat_g': 'fat_g_per_100g',
        'carbs_g': 'carbs_g_per_100g',
        'fiber_g': 'fiber_g_per_100g',
        'sodium_mg': 'sodium_mg_per_100g',
        'potassium_mg': 'potassium_mg_per_100g',
        'phosphorus_mg': 'phosphorus_mg_per_100g',
        'energy_kj': 'energy_kj_per_100g'
    })
    
    # Calculate calories from kJ (1 kJ = 0.239 kcal)
    df['calories_per_100g'] = (df['energy_kj_per_100g'] * 0.239).round(0).astype(int)
    
    # Reorder columns
    columns = [
        'code', 'ingredient', 'category',
        'sodium_mg_per_100g', 'potassium_mg_per_100g', 'phosphorus_mg_per_100g',
        'protein_g_per_100g', 'carbs_g_per_100g', 'fat_g_per_100g', 'fiber_g_per_100g',
        'moisture', 'energy_kj_per_100g', 'calories_per_100g'
    ]
    df = df[columns]
    
    # Save to CSV
    output_path = Path(__file__).parent / "ifct_database.csv"
    df.to_csv(output_path, index=False)
    
    print(f"Created IFCT database with {len(df)} ingredients")
    print(f"Saved to: {output_path}")
    print(f"\nCategories:")
    print(df['category'].value_counts())
    
    return df

if __name__ == "__main__":
    df = create_ifct_database()
    print(f"\nSample data:")
    print(df.head(10))
